        <h2 class="section-title fade-in">Perguntas Frequentes</h2>
        <div class="faq-container">
            <div class="faq-item fade-in">
                <button class="faq-question">
                    O que é TDAH e quais são os principais sintomas?
                    <i class="fas fa-plus"></i>
                </button>
                <div class="faq-answer">
                    <div class="faq-answer-content">
                        O TDAH (Transtorno do Déficit de Atenção e Hiperatividade) é um transtorno neurobiológico
                        caracterizado por sintomas de desatenção, hiperatividade e impulsividade. Os principais
                        sintomas incluem dificuldade de concentração, esquecimento frequente, inquietação,
                        impulsividade nas decisões e dificuldade em organizar tarefas.
                    </div>
                </div>
            </div>
            <div class="faq-item fade-in">
                <button class="faq-question">
                    Como é feito o diagnóstico do TDAH?
                    <i class="fas fa-plus"></i>
                </button>
                <div class="faq-answer">
                    <div class="faq-answer-content">
                        O diagnóstico do TDAH é clínico, baseado nos critérios do DSM-5. É realizada uma avaliação
                        completa incluindo histórico médico, entrevista detalhada, questionários específicos e,
                        quando necessário, testes neuropsicológicos. O processo envolve a análise dos sintomas em
                        diferentes contextos (casa, trabalho, escola).
                    </div>
                </div>
            </div>
            <div class="faq-item fade-in">
                <button class="faq-question">
                    O TDAH tem cura ou é um tratamento para toda a vida?
                    <i class="fas fa-plus"></i>
                </button>
                <div class="faq-answer">
                    <div class="faq-answer-content">
                        O TDAH não tem cura, mas tem tratamento eficaz. Com o tratamento adequado, é possível
                        controlar os sintomas e ter uma vida normal e produtiva. O tratamento pode incluir
                        medicação, terapia comportamental, mudanças no estilo de vida e estratégias de organização.
                        Muitas pessoas conseguem reduzir ou até suspender a medicação ao longo do tempo.
                    </div>
                </div>
            </div>
            <div class="faq-item fade-in">
                <button class="faq-question">
                    Quais são os tipos de medicamentos para TDAH?
                    <i class="fas fa-plus"></i>
                </button>
                <div class="faq-answer">
                    <div class="faq-answer-content">
                        Existem dois tipos principais de medicamentos: estimulantes (como metilfenidato - Ritalina,
                        Concerta) e não estimulantes (como atomoxetina). A escolha depende de fatores individuais
                        como idade, sintomas específicos, possíveis efeitos colaterais e resposta ao tratamento.
                        Todos devem ser prescritos e acompanhados por um médico especialista.
                    </div>
                </div>
            </div>
            <div class="faq-item fade-in">
                <button class="faq-question">
                    Como funciona a consulta online?
                    <i class="fas fa-plus"></i>
                </button>
                <div class="faq-answer">
                    <div class="faq-answer-content">
                        A consulta online é realizada por telemedicina, com a mesma qualidade do atendimento
                        presencial. Após o agendamento, você recebe um link para a videoconferência. As receitas
                        controladas são enviadas por correio (SEDEX ou carta registrada) e outros documentos são
                        enviados digitalmente via WhatsApp. Atendo pacientes em todo o Brasil.
                    </div>
                </div>
            </div>
            <div class="faq-item fade-in">
                <button class="faq-question">
                    O TDAH afeta apenas crianças ou adultos também podem ter?
                    <i class="fas fa-plus"></i>
                </button>
                <div class="faq-answer">
                    <div class="faq-answer-content">
                        O TDAH pode afetar tanto crianças quanto adultos. Muitos adultos descobrem que têm TDAH
                        depois de seus filhos serem diagnosticados. Em adultos, os sintomas podem se manifestar como
                        dificuldades no trabalho, problemas de relacionamento, desorganização financeira e baixa
                        autoestima. O diagnóstico e tratamento em adultos são igualmente importantes.
                    </div>
                </div>
            </div>
            <div class="faq-item fade-in">
                <button class="faq-question">
                    Quais são os efeitos colaterais dos medicamentos para TDAH?
                    <i class="fas fa-plus"></i>
                </button>
                <div class="faq-answer">
                    <div class="faq-answer-content">
                        Os efeitos colaterais mais comuns incluem diminuição do apetite, dificuldade para dormir,
                        dor de cabeça e, raramente, alterações no humor. A maioria dos efeitos é leve e temporária.
                        O acompanhamento médico regular é essencial para monitorar e ajustar a medicação conforme
                        necessário, minimizando os efeitos colaterais.
                    </div>
                </div>
            </div>
            <div class="faq-item fade-in">
                <button class="faq-question">
                    É possível tratar TDAH sem medicamentos?
                    <i class="fas fa-plus"></i>
                </button>
                <div class="faq-answer">
                    <div class="faq-answer-content">
                        Sim, existem abordagens não medicamentosas que podem ser eficazes, especialmente em casos
                        leves ou como complemento ao tratamento medicamentoso. Incluem terapia comportamental,
                        técnicas de organização, exercícios físicos regulares, alimentação balanceada, técnicas de
                        mindfulness e mudanças no ambiente de trabalho ou estudo. O ideal é uma abordagem integrada.
                    </div>
                </div>
            </div>
        </div>
    </div>
</section>